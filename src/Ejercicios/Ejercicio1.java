package Ejercicios;

public class Ejercicio1 {

	public static void main(String[] args) {

		for (int i = 1; i <= 100; i++) {
			System.out.println(i);
		}
		System.out.println("1");
		int i=1;
		while (i <= 100) {
			System.out.println(i);
			i++;
		}
		System.out.println("2");
		int j=0;
		do {
			j++;
			System.out.println(j);
		}while(j<100);
		
	}

}
